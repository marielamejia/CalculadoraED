/**
 * Ingresa la operacion igual (=) y finaliza una etapa del metodo
 * @param evt 
 */
    private void btIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btIgualActionPerformed
        // TODO add your handling code here:
        if(!cad.equals("")){
            agregaArrayList(cad);
            setUniversal(cad);
        }
        setCadena("");
        if(infijo.isEmpty())
            this.panelResultado.setText(univ);
        else{
            if(!Calculadora.revisaArray(infijo))
                this.panelResultado.setText("SYNTAX ERROR");
            else{
                setUniversal(Calculadora.imprimeArray(infijo));
                ArrayList<String> postfijo = Calculadora.escribePostfijo(infijo);
                PilaA<Double> aux = new PilaA();
                int i=0;
                boolean bandera = true;
                while(i<postfijo.size() && bandera){
                    if(!Calculadora.esOperador(postfijo.get(i)))
                        aux.push(Double.parseDouble(postfijo.get(i)));
                    else{
                        double b = aux.pop();
                        double a = aux.pop();
                        switch(postfijo.get(i)){
                            case "+" -> aux.push(a+b);
                            case "-" -> aux.push(a-b);
                            case "*" -> aux.push(a*b);
                            case "/" -> {
                                if(b == 0){
                                    bandera = false;
                                    this.panelResultado.setText("MATH ERROR");
                                }
                                else
                                    aux.push(a/b);
                            }   
                            case "^" -> aux.push(Math.pow(a,b));
                        }
                    }
                    i++;
                }
                if(bandera == true && i == postfijo.size())
                    this.panelResultado.setText("" + aux.pop());
            }  
        }
    }//GEN-LAST:event_btIgualActionPerformed
