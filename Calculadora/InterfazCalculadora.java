/**
 * Elimina ultimo digito agregado a la cadena 
 * @param evt 
 */
    private void btBorrarUltimoElementoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBorrarUltimoElementoActionPerformed
        if(infijo.isEmpty())
            if(cad.equals(""))
                setUniversal("");
            else{
                setCadena("");
                setUniversal(cad);
            }
        
        else
            if(cad.equals("")){
                infijo.remove(infijo.size() - 1);
                setUniversal(Calculadora.imprimeArray(infijo));
            }
            else{
                setCadena("");
                setUniversal(Calculadora.imprimeArray(infijo) + cad);
            }
        this.panelPrincipal.setText(univ);
    }//GEN-LAST:event_btBorrarUltimoElementoActionPerformed
/**
 * Cambia de signo el ultimo valor numerico para utilizarse como negativo
 * @param evt 
 */
    private void btCambiaSignoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCambiaSignoActionPerformed
        if(!cad.equals("")){
            setCadena(Calculadora.cambiaDeSigno(cad));
            setUniversal(Calculadora.imprimeArray(infijo) + cad);
        }
        else
            if(!infijo.isEmpty())
                if(!Calculadora.esOperador(infijo.get( infijo.size() - 1 )) && !infijo.get( infijo.size() - 1 ).equals("(") && !infijo.get( infijo.size() - 1 ).equals(")")){
                    modificaArray(Calculadora.cambiaDeSigno(infijo.get(infijo.size()-1)), infijo.size()-1);
                    setUniversal(Calculadora.imprimeArray(infijo));
                }
        this.panelPrincipal.setText(univ);
    }//GEN-LAST:event_btCambiaSignoActionPerformed

